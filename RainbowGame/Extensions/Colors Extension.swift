import UIKit

extension UIColor {
    
    func getRedColor() -> UIColor {
        return UIColor(red: 255/255, green: 69/255, blue: 61/255, alpha: 1)
    }
    
    
    func getLightBlueColor() -> UIColor {
        return UIColor(red: 166/255, green: 228/255, blue: 251/255, alpha: 1)
    }
    
    
    func getOrangeColor() -> UIColor {
        return UIColor(red: 255/255, green: 167/255, blue: 33/255, alpha: 1)
    }
    
    
    func getYellowColor() -> UIColor {
        return UIColor(red: 253/255, green: 215/255, blue: 58/255, alpha: 1)
    }
    
    
    func getGreenColor() -> UIColor {
        return UIColor(red: 4/255, green: 182/255, blue: 96/255, alpha: 1)
    }
    
    
    func getBlueColor() -> UIColor {
        return UIColor(red: 5/255, green: 155/255, blue: 221/255, alpha: 1)
    }
    
    
    func getDarkBlueColor() -> UIColor {
        return UIColor(red: 2/255, green: 117/255, blue: 190/255, alpha: 1)
    }
    
    
    func getPurpleColor() -> UIColor {
        return UIColor(red: 148/255, green: 74/255, blue: 156/255, alpha: 1)
    }
    
    func getBackgroundColor() -> UIColor {
        return UIColor(red: 166/255, green: 228/255, blue: 251/255, alpha: 1)
    }
}
